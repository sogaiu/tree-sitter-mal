      if (lookahead == '+' ||
          lookahead == '-') ADVANCE(25);
      if (('0' <= lookahead && lookahead <= '9')) ADVANCE(10);
      if (!sym_kwd_lit_character_set_1(lookahead)) ADVANCE(26);
      END_STATE();
    case 24:
      ACCEPT_TOKEN(aux_sym_sym_lit_token1);
      if (('0' <= lookahead && lookahead <= '9')) ADVANCE(8);
